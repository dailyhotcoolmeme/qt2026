import fs from "node:fs";
import path from "node:path";

const SUPABASE_URL = String(process.env.SUPABASE_URL || "").trim();
const SUPABASE_SERVICE_ROLE_KEY = String(process.env.SUPABASE_SERVICE_ROLE_KEY || "").trim();
const OUTPUT_PATH = process.argv[2]
  ? path.resolve(process.argv[2])
  : path.resolve("cloudflare/d1/seed/bible_audio_metadata.sql");

if (!SUPABASE_URL || !SUPABASE_SERVICE_ROLE_KEY) {
  console.error("Missing SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY");
  process.exit(1);
}

function escapeSqlString(value) {
  if (value === null || value === undefined) return "NULL";
  return `'${String(value).replace(/'/g, "''")}'`;
}

function toInteger(value, fallback = 0) {
  const parsed = Number(value);
  if (!Number.isFinite(parsed)) return fallback;
  return Math.trunc(parsed);
}

async function fetchBatch(offset, limit) {
  const url = new URL("/rest/v1/bible_audio_metadata", SUPABASE_URL);
  url.searchParams.set("select", "testament,book_id,chapter,audio_url,verse_timings,duration,created_at");
  url.searchParams.set("order", "book_id.asc,chapter.asc");
  url.searchParams.set("limit", String(limit));
  url.searchParams.set("offset", String(offset));

  const response = await fetch(url.toString(), {
    method: "GET",
    headers: {
      apikey: SUPABASE_SERVICE_ROLE_KEY,
      Authorization: `Bearer ${SUPABASE_SERVICE_ROLE_KEY}`,
    },
  });

  if (!response.ok) {
    const body = await response.text();
    throw new Error(`Supabase fetch failed (${response.status}): ${body.slice(0, 300)}`);
  }

  return response.json();
}

async function main() {
  const rows = [];
  const limit = 1000;
  let offset = 0;

  while (true) {
    const batch = await fetchBatch(offset, limit);
    if (!Array.isArray(batch) || batch.length === 0) break;
    rows.push(...batch);
    offset += batch.length;
    process.stdout.write(`Fetched ${rows.length} rows\r`);
    if (batch.length < limit) break;
  }
  process.stdout.write("\n");

  const statements = [];
  statements.push("-- Generated by scripts/export-bible-audio-metadata-for-d1.mjs");
  statements.push("BEGIN TRANSACTION;");
  statements.push("DELETE FROM bible_audio_metadata;");

  for (const row of rows) {
    const testament = row.testament ?? null;
    const bookId = toInteger(row.book_id);
    const chapter = toInteger(row.chapter);
    const audioUrl = row.audio_url ?? "";
    const verseTimings = row.verse_timings === null ? null : JSON.stringify(row.verse_timings);
    const duration = toInteger(row.duration, 0);
    const createdAt = row.created_at ?? null;

    statements.push(
      [
        "INSERT INTO bible_audio_metadata",
        "(testament, book_id, chapter, audio_url, verse_timings, duration, created_at, updated_at)",
        "VALUES",
        `(${escapeSqlString(testament)}, ${bookId}, ${chapter}, ${escapeSqlString(audioUrl)}, ${escapeSqlString(
          verseTimings,
        )}, ${duration}, ${escapeSqlString(createdAt)}, datetime('now'));`,
      ].join(" "),
    );
  }

  statements.push("COMMIT;");

  fs.mkdirSync(path.dirname(OUTPUT_PATH), { recursive: true });
  fs.writeFileSync(OUTPUT_PATH, `${statements.join("\n")}\n`, "utf8");
  console.log(`Wrote ${rows.length} rows to ${OUTPUT_PATH}`);
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
